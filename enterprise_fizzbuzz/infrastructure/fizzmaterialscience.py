class ThermalConductivityEngine:
    """Estimates thermal conductivity using the Debye model.

    Thermal conductivity is computed from the phonon contribution using
    k = (1/3) * C_v * v_s * l, where C_v is the volumetric heat capacity,
    v_s is the speed of sound, and l is the phonon mean free path.
    """

    def compute(
        self,
        debye_temp_k: float,
        temperature_k: float = ROOM_TEMPERATURE_K,
        lattice_param_a: float = 3.615,
    ) -> float:
        """Estimate thermal conductivity in W/(m*K)."""
        if temperature_k <= 0:
            from enterprise_fizzbuzz.domain.exceptions.fizzmaterialscience import (
                ThermalConductivityError,
            )
            raise ThermalConductivityError("computed_material", 0.0)

        # Volumetric heat capacity (Dulong-Petit limit approximation)
        x = debye_temp_k / temperature_k
        if x < 0.1:
            cv = 3.0 * GAS_CONSTANT
        else:
            cv = 3.0 * GAS_CONSTANT * self._debye_cv_ratio(x)

        # Speed of sound (m/s) — estimated from Debye temperature
        v_s = debye_temp_k * BOLTZMANN_EV * 1.602e-19 / (
            1.055e-34 * (6.0 * math.pi ** 2 / (lattice_param_a * 1e-10) ** 3) ** (1.0 / 3.0)
        )
        v_s = abs(v_s)

        # Mean free path (simplified — inversely proportional to T at high T)
        mfp = lattice_param_a * 1e-10 * 100.0 * (debye_temp_k / temperature_k)

        # k = (1/3) * C_v * v_s * mfp, convert to W/(m*K)
        molar_volume = (lattice_param_a * 1e-10) ** 3 * 6.022e23
        cv_volumetric = cv / molar_volume if molar_volume > 0 else cv

        conductivity = (1.0 / 3.0) * cv_volumetric * v_s * mfp

        if conductivity < 0:
            from enterprise_fizzbuzz.domain.exceptions.fizzmaterialscience import (
                ThermalConductivityError,
            )
            raise ThermalConductivityError("computed_material", conductivity)

        return conductivity

    @staticmethod
    def _debye_cv_ratio(x: float) -> float:
        """Approximate Debye heat capacity ratio C_v / 3NkB."""
        if x <= 0:
            return 1.0
        if x > 20:
            return (4.0 * math.pi ** 4) / (5.0 * x ** 3)
        # Numerical integration (trapezoidal) of Debye function
        n_steps = 100
        dt = x / n_steps
        integral = 0.0
        for i in range(1, n_steps + 1):
            t = i * dt
            if t > 500:
                break
            exp_t = math.exp(t)
            integrand = t ** 4 * exp_t / (exp_t - 1.0) ** 2
            integral += integrand * dt
        return (3.0 / x ** 3) * integral


# ---------------------------------------------------------------------------
# Young's Modulus Calculator
# ---------------------------------------------------------------------------


class YoungModulusCalculator:
    """Calculates Young's modulus from lattice parameters.

    Uses the empirical Blackman relation between elastic constants and
    lattice geometry. For cubic crystals, E = C11 - 2*C12^2 / (C11 + C12),
    where C11 and C12 are estimated from the cohesive energy and lattice
    constant.
    """

    # Approximate elastic constants (GPa) by crystal structure
    _C11_TABLE: dict[CrystalStructure, float] = {
        CrystalStructure.FCC: 168.0,
        CrystalStructure.BCC: 231.0,
        CrystalStructure.HCP: 162.0,
        CrystalStructure.SIMPLE_CUBIC: 100.0,
        CrystalStructure.DIAMOND: 165.0,
    }

    _C12_TABLE: dict[CrystalStructure, float] = {
        CrystalStructure.FCC: 121.0,
        CrystalStructure.BCC: 135.0,
        CrystalStructure.HCP: 92.0,
        CrystalStructure.SIMPLE_CUBIC: 50.0,
        CrystalStructure.DIAMOND: 64.0,
    }

    def compute(self, lattice: LatticeParameters) -> float:
        """Compute Young's modulus in GPa."""
        c11 = self._C11_TABLE.get(lattice.structure, 100.0)
        c12 = self._C12_TABLE.get(lattice.structure, 50.0)

        # Scale by lattice constant ratio
        a0 = {
            CrystalStructure.FCC: 3.615,
            CrystalStructure.BCC: 2.870,
            CrystalStructure.HCP: 3.209,
            CrystalStructure.SIMPLE_CUBIC: 2.650,
            CrystalStructure.DIAMOND: 5.431,
        }.get(lattice.structure, 3.0)

        scale = (a0 / lattice.a) ** 3 if lattice.a > 0 else 1.0
        c11 *= scale
        c12 *= scale

        # Voigt-Reuss-Hill average for polycrystalline Young's modulus
        denom = c11 + c12
        if abs(denom) < 1e-12:
            from enterprise_fizzbuzz.domain.exceptions.fizzmaterialscience import (
                YoungModulusError,
            )
            raise YoungModulusError("computed_material", 0.0)

        e_vrh = (c11 - c12) * (c11 + 2.0 * c12) / (c11 + c12)

        if e_vrh <= 0:
            from enterprise_fizzbuzz.domain.exceptions.fizzmaterialscience import (
                YoungModulusError,
            )
            raise YoungModulusError("computed_material", e_vrh)

        return e_vrh


