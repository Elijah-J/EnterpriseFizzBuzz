class AlloyCompositionAnalyzer:
    """Analyzes alloy compositions derived from FizzBuzz classifications.

    The composition is derived from the number's divisibility profile:
    - Fizz component (Fz): fraction based on divisibility by 3
    - Buzz component (Bz): fraction based on divisibility by 5
    - Neutral component (N): remainder

    This ternary alloy system (Fz-Bz-N) has rich phase behavior that
    directly impacts the mechanical properties of the FizzBuzz output.
    """

    def compute_composition(
        self, number: int, is_fizz: bool, is_buzz: bool
    ) -> AlloyComposition:
        """Compute alloy composition from FizzBuzz classification."""
        fizz_frac = 0.0
        buzz_frac = 0.0

        if is_fizz and is_buzz:
            fizz_frac = 0.4
            buzz_frac = 0.4
        elif is_fizz:
            fizz_frac = 0.6
            buzz_frac = 0.1
        elif is_buzz:
            fizz_frac = 0.1
            buzz_frac = 0.6
        else:
            digit_sum = sum(int(d) for d in str(abs(number))) if number != 0 else 0
            fizz_frac = (digit_sum % 10) / 50.0
            buzz_frac = ((digit_sum * 7) % 10) / 50.0

        neutral_frac = 1.0 - fizz_frac - buzz_frac
        neutral_frac = max(0.0, neutral_frac)

        total = fizz_frac + buzz_frac + neutral_frac
        if total > 0:
            fizz_frac /= total
            buzz_frac /= total
            neutral_frac /= total

        comp = AlloyComposition(
            components={"Fz": fizz_frac, "Bz": buzz_frac, "N": neutral_frac}
        )

        if not comp.is_valid:
            from enterprise_fizzbuzz.domain.exceptions.fizzmaterialscience import (
                AlloyCompositionError,
            )
            raise AlloyCompositionError(
                comp.components, "component fractions do not sum to 1.0"
            )

        return comp


