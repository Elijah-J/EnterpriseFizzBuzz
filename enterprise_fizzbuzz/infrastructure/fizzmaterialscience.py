class CrystalLatticeEngine:
    """Constructs and analyzes crystal lattice structures.

    Maps FizzBuzz classification to crystal structures: Fizz numbers
    crystallize as FCC, Buzz numbers as BCC, FizzBuzz as HCP, plain
    numbers as simple cubic, and primes as diamond cubic. The lattice
    constant scales with the number magnitude.
    """

    def build_lattice(self, number: int, is_fizz: bool, is_buzz: bool) -> LatticeParameters:
        """Construct a crystal lattice for the given FizzBuzz classification."""
        structure = self._classify_structure(number, is_fizz, is_buzz)
        a = self._compute_lattice_constant(number, structure)

        atoms = {
            CrystalStructure.FCC: 4,
            CrystalStructure.BCC: 2,
            CrystalStructure.HCP: 6,
            CrystalStructure.SIMPLE_CUBIC: 1,
            CrystalStructure.DIAMOND: 8,
        }

        lattice = LatticeParameters(
            structure=structure,
            a=a,
            b=a,
            c=a if structure != CrystalStructure.HCP else a * 1.633,
            alpha_deg=90.0,
            beta_deg=90.0,
            gamma_deg=90.0 if structure != CrystalStructure.HCP else 120.0,
            atoms_per_cell=atoms.get(structure, 1),
        )

        if lattice.volume <= 0:
            from enterprise_fizzbuzz.domain.exceptions.fizzmaterialscience import (
                LatticeConstructionError,
            )
            raise LatticeConstructionError(
                structure.name, "unit cell volume is non-positive"
            )

        return lattice

    def _classify_structure(
        self, number: int, is_fizz: bool, is_buzz: bool
    ) -> CrystalStructure:
        if is_fizz and is_buzz:
            return CrystalStructure.HCP
        elif is_fizz:
            return CrystalStructure.FCC
        elif is_buzz:
            return CrystalStructure.BCC
        elif self._is_prime(number):
            return CrystalStructure.DIAMOND
        else:
            return CrystalStructure.SIMPLE_CUBIC

    def _compute_lattice_constant(
        self, number: int, structure: CrystalStructure
    ) -> float:
        """Compute lattice constant in Angstroms, scaled by number magnitude."""
        base = {
            CrystalStructure.FCC: 3.615,
            CrystalStructure.BCC: 2.870,
            CrystalStructure.HCP: 3.209,
            CrystalStructure.SIMPLE_CUBIC: 2.650,
            CrystalStructure.DIAMOND: 5.431,
        }
        a0 = base.get(structure, 3.0)
        scale = 1.0 + 0.001 * (number % 100)
        return a0 * scale

    @staticmethod
    def _is_prime(n: int) -> bool:
        if n < 2:
            return False
        if n < 4:
            return True
        if n % 2 == 0 or n % 3 == 0:
            return False
        i = 5
        while i * i <= n:
            if n % i == 0 or n % (i + 2) == 0:
                return False
            i += 6
        return True

    def nearest_neighbor_distance(self, lattice: LatticeParameters) -> float:
        """Compute nearest-neighbor distance in Angstroms."""
        if lattice.structure == CrystalStructure.FCC:
            return lattice.a / math.sqrt(2.0)
        elif lattice.structure == CrystalStructure.BCC:
            return lattice.a * math.sqrt(3.0) / 2.0
        elif lattice.structure == CrystalStructure.HCP:
            return lattice.a
        elif lattice.structure == CrystalStructure.DIAMOND:
            return lattice.a * math.sqrt(3.0) / 4.0
        else:
            return lattice.a

    def coordination_number(self, structure: CrystalStructure) -> int:
        """Return the coordination number for the given structure."""
        cn = {
            CrystalStructure.FCC: 12,
            CrystalStructure.BCC: 8,
            CrystalStructure.HCP: 12,
            CrystalStructure.SIMPLE_CUBIC: 6,
            CrystalStructure.DIAMOND: 4,
        }
        return cn.get(structure, 6)


