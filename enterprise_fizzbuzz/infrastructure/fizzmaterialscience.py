# Stress-Strain Engine
# ---------------------------------------------------------------------------


class StressStrainEngine:
    """Computes stress-strain curves for FizzBuzz materials.

    Uses a combined elastic-plastic constitutive model with linear
    hardening. The elastic region follows Hooke's law (sigma = E * epsilon),
    the plastic region follows the Ramberg-Osgood relation, and fracture
    occurs at the ultimate tensile strength.
    """

    def compute_curve(
        self,
        youngs_modulus_gpa: float,
        yield_stress_mpa: float,
        hardening_exponent: float = 0.15,
        num_points: int = 50,
    ) -> StressStrainCurve:
        """Generate a stress-strain curve."""
        if youngs_modulus_gpa <= 0:
            from enterprise_fizzbuzz.domain.exceptions.fizzmaterialscience import (
                YoungModulusError,
            )
            raise YoungModulusError("computed_material", youngs_modulus_gpa)

        e_mpa = youngs_modulus_gpa * 1000.0
        yield_strain = yield_stress_mpa / e_mpa
        uts_mpa = yield_stress_mpa * (1.0 + hardening_exponent * 10.0)
        fracture_strain = yield_strain * 20.0

        points: list[StressStrainPoint] = []

        for i in range(num_points):
            strain = fracture_strain * i / (num_points - 1)

            if strain <= yield_strain:
                stress = e_mpa * strain
                mode = DeformationMode.ELASTIC
            elif strain <= fracture_strain * 0.9:
                plastic_strain = strain - yield_strain
                stress = yield_stress_mpa + (
                    uts_mpa - yield_stress_mpa
                ) * (1.0 - math.exp(-10.0 * plastic_strain / fracture_strain))
                mode = DeformationMode.PLASTIC
            else:
                frac_progress = (strain - fracture_strain * 0.9) / (
                    fracture_strain * 0.1
                )
                stress = uts_mpa * (1.0 - frac_progress * 0.3)
                mode = DeformationMode.FRACTURE

            if stress < 0:
                from enterprise_fizzbuzz.domain.exceptions.fizzmaterialscience import (
                    StressStrainError,
                )
                raise StressStrainError(
                    stress, strain, "negative stress in tensile loading"
                )

            points.append(StressStrainPoint(strain=strain, stress_mpa=stress, mode=mode))

        return StressStrainCurve(
            points=points,
            youngs_modulus_gpa=youngs_modulus_gpa,
            yield_stress_mpa=yield_stress_mpa,
            ultimate_stress_mpa=uts_mpa,
            fracture_strain=fracture_strain,
        )


