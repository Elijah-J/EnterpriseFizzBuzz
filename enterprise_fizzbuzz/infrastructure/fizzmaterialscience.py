class MaterialScienceMiddleware(IMiddleware):
    """Middleware that performs material science analysis for each evaluation.

    Each number is characterized as a crystalline material with specific
    lattice structure, mechanical properties, and thermal behavior. The
    results are attached to the processing context metadata for
    consumption by downstream middleware.

    Priority 292 positions this in the physical sciences tier.
    """

    def __init__(self) -> None:
        self._engine = MaterialScienceEngine()
        self._evaluations = 0

    def get_name(self) -> str:
        return "fizzmaterialscience"

    def get_priority(self) -> int:
        return 292

    @property
    def engine(self) -> MaterialScienceEngine:
        return self._engine

    def process(
        self,
        context: ProcessingContext,
        next_handler: Callable[[ProcessingContext], ProcessingContext],
    ) -> ProcessingContext:
        result = next_handler(context)

        number = context.number
        is_fizz = False
        is_buzz = False
        if result.results:
            latest = result.results[-1]
            is_fizz = latest.is_fizz
            is_buzz = latest.is_buzz

        try:
            props = self._engine.analyze_number(number, is_fizz, is_buzz)
            self._evaluations += 1

            result.metadata["material_science"] = {
                "crystal_structure": props.lattice.structure.name,
                "lattice_constant_a": round(props.lattice.a, 4),
                "packing_fraction": round(props.lattice.packing_fraction, 4),
                "youngs_modulus_gpa": round(props.youngs_modulus_gpa, 2),
                "thermal_conductivity_w_mk": round(props.thermal_conductivity_w_mk, 4),
                "phase": props.phase.name,
                "yield_stress_mpa": round(props.stress_strain.yield_stress_mpa, 2),
                "alloy_composition": {
                    k: round(v, 4) for k, v in props.alloy.components.items()
                },
                "debye_temperature_k": props.debye_temperature_k,
            }

            logger.debug(
                "FizzMaterialScience: number=%d structure=%s E=%.2f GPa",
                number,
                props.lattice.structure.name,
                props.youngs_modulus_gpa,
            )

        except Exception:
            logger.exception(
                "FizzMaterialScience: analysis failed for number %d", number
            )

        return result
