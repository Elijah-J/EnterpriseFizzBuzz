class MaterialScienceEngine:
    """Integrates all material science analysis components.

    Performs a complete material characterization for each FizzBuzz
    evaluation: crystal structure determination, mechanical property
    calculation, thermal analysis, and alloy composition profiling.
    """

    def __init__(self) -> None:
        self.lattice_engine = CrystalLatticeEngine()
        self.stress_strain_engine = StressStrainEngine()
        self.phase_diagram_engine = PhaseDiagramEngine()
        self.thermal_engine = ThermalConductivityEngine()
        self.youngs_engine = YoungModulusCalculator()
        self.alloy_analyzer = AlloyCompositionAnalyzer()
        self._analysis_count = 0

    def analyze_number(
        self, number: int, is_fizz: bool, is_buzz: bool
    ) -> MaterialProperties:
        """Perform full material characterization for a FizzBuzz number."""
        self._analysis_count += 1

        lattice = self.lattice_engine.build_lattice(number, is_fizz, is_buzz)

        youngs_modulus = self.youngs_engine.compute(lattice)

        yield_stress = DEFAULT_YIELD_STRESS * (1.0 + 0.01 * (number % 50))
        stress_strain = self.stress_strain_engine.compute_curve(
            youngs_modulus_gpa=youngs_modulus,
            yield_stress_mpa=yield_stress,
        )

        debye_t = {
            CrystalStructure.FCC: DEBYE_TEMPERATURE_FCC,
            CrystalStructure.BCC: DEBYE_TEMPERATURE_BCC,
            CrystalStructure.HCP: DEBYE_TEMPERATURE_HCP,
        }.get(lattice.structure, 350.0)

        thermal_k = self.thermal_engine.compute(
            debye_temp_k=debye_t,
            lattice_param_a=lattice.a,
        )

        composition_frac = (number % 100) / 100.0
        phase_pt = self.phase_diagram_engine.compute_phase(
            ROOM_TEMPERATURE_K, composition_frac
        )

        alloy = self.alloy_analyzer.compute_composition(number, is_fizz, is_buzz)

        return MaterialProperties(
            lattice=lattice,
            stress_strain=stress_strain,
            thermal_conductivity_w_mk=thermal_k,
            youngs_modulus_gpa=youngs_modulus,
            phase=phase_pt.phase,
            alloy=alloy,
            debye_temperature_k=debye_t,
        )

    @property
    def analysis_count(self) -> int:
        return self._analysis_count


