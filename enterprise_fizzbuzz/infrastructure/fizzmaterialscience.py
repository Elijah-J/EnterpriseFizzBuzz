class PhaseDiagramEngine:
    """Constructs binary phase diagrams for FizzBuzz alloy systems.

    The Fizz-Buzz binary system has a simple eutectic phase diagram:
    the alpha phase (Fizz-rich) and beta phase (Buzz-rich) are separated
    by a two-phase region, with a eutectic point at the composition
    corresponding to FizzBuzz (x = 3/15 = 0.2).
    """

    def __init__(
        self,
        melting_a_k: float = 1358.0,  # Copper-like
        melting_b_k: float = 1811.0,  # Iron-like
        eutectic_temp_k: float = 1100.0,
        eutectic_comp: float = 0.2,
    ) -> None:
        self._melting_a = melting_a_k
        self._melting_b = melting_b_k
        self._eutectic_temp = eutectic_temp_k
        self._eutectic_comp = eutectic_comp

    def compute_phase(
        self, temperature_k: float, composition: float
    ) -> PhaseDiagramPoint:
        """Determine the thermodynamic phase at a given T-x point."""
        if composition < 0.0 or composition > 1.0:
            from enterprise_fizzbuzz.domain.exceptions.fizzmaterialscience import (
                PhaseDiagramError,
            )
            raise PhaseDiagramError(
                temperature_k, composition, "composition must be in [0, 1]"
            )

        if temperature_k < 0:
            from enterprise_fizzbuzz.domain.exceptions.fizzmaterialscience import (
                PhaseDiagramError,
            )
            raise PhaseDiagramError(
                temperature_k, composition, "temperature must be non-negative"
            )

        liquidus_t = self._liquidus_temperature(composition)
        gibbs = self._gibbs_energy(temperature_k, composition)

        if temperature_k > liquidus_t:
            phase = PhaseType.LIQUID
        elif composition < self._eutectic_comp:
            phase = PhaseType.ALPHA
        elif composition > 1.0 - self._eutectic_comp:
            phase = PhaseType.BETA
        else:
            if temperature_k > self._eutectic_temp:
                phase = PhaseType.GAMMA
            else:
                phase = PhaseType.ALPHA

        return PhaseDiagramPoint(
            temperature_k=temperature_k,
            composition=composition,
            phase=phase,
            gibbs_energy_j=gibbs,
        )

    def _liquidus_temperature(self, composition: float) -> float:
        """Compute the liquidus temperature at a given composition."""
        t_a = self._melting_a
        t_b = self._melting_b
        t_e = self._eutectic_temp
        x_e = self._eutectic_comp

        if composition <= x_e:
            frac = composition / x_e if x_e > 0 else 0.0
            return t_a - (t_a - t_e) * frac
        else:
            frac = (composition - x_e) / (1.0 - x_e)
            return t_e + (t_b - t_e) * frac

    def _gibbs_energy(self, temperature_k: float, composition: float) -> float:
        """Compute Gibbs free energy of mixing (simplified regular solution)."""
        if composition <= 0 or composition >= 1.0:
            return 0.0
        omega = 20000.0  # Interaction parameter (J/mol)
        x = composition
        mixing_entropy = GAS_CONSTANT * temperature_k * (
            x * math.log(x) + (1.0 - x) * math.log(1.0 - x)
        )
        mixing_enthalpy = omega * x * (1.0 - x)
        return mixing_enthalpy + mixing_entropy

    def compute_phase_boundaries(
        self, num_points: int = 50
    ) -> list[PhaseDiagramPoint]:
        """Compute the liquidus line across the full composition range."""
        points: list[PhaseDiagramPoint] = []
        for i in range(num_points):
            x = i / (num_points - 1)
            t = self._liquidus_temperature(x)
            pt = self.compute_phase(t - 1.0, x)
            points.append(
                PhaseDiagramPoint(
                    temperature_k=t,
                    composition=x,
                    phase=PhaseType.LIQUID,
                    gibbs_energy_j=pt.gibbs_energy_j,
                )
            )
        return points


