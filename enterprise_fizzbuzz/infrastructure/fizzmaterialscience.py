@dataclass
class LatticeParameters:
    """Crystal lattice parameters.

    Defines the unit cell of a Bravais lattice in terms of lattice
    constants (a, b, c) and angles (alpha, beta, gamma).
    """
    structure: CrystalStructure = CrystalStructure.FCC
    a: float = 3.615  # Angstroms (Cu default)
    b: float = 3.615
    c: float = 3.615
    alpha_deg: float = 90.0
    beta_deg: float = 90.0
    gamma_deg: float = 90.0
    atoms_per_cell: int = 4  # FCC default

    @property
    def volume(self) -> float:
        """Unit cell volume in cubic Angstroms."""
        alpha = math.radians(self.alpha_deg)
        beta = math.radians(self.beta_deg)
        gamma = math.radians(self.gamma_deg)
        cos_a = math.cos(alpha)
        cos_b = math.cos(beta)
        cos_g = math.cos(gamma)
        factor = math.sqrt(
            1.0 - cos_a ** 2 - cos_b ** 2 - cos_g ** 2
            + 2.0 * cos_a * cos_b * cos_g
        )
        return self.a * self.b * self.c * factor

    @property
    def packing_fraction(self) -> float:
        """Atomic packing fraction for the lattice type."""
        packing = {
            CrystalStructure.FCC: math.pi / (3.0 * math.sqrt(2.0)),
            CrystalStructure.BCC: math.pi * math.sqrt(3.0) / 8.0,
            CrystalStructure.HCP: math.pi / (3.0 * math.sqrt(2.0)),
            CrystalStructure.SIMPLE_CUBIC: math.pi / 6.0,
            CrystalStructure.DIAMOND: math.pi * math.sqrt(3.0) / 16.0,
        }
        return packing.get(self.structure, math.pi / 6.0)


@dataclass
class StressStrainPoint:
    """A single point on the stress-strain curve."""
    strain: float = 0.0
    stress_mpa: float = 0.0
    mode: DeformationMode = DeformationMode.ELASTIC


@dataclass
class StressStrainCurve:
    """Complete stress-strain curve for a material."""
    points: list[StressStrainPoint] = field(default_factory=list)
    youngs_modulus_gpa: float = 0.0
    yield_stress_mpa: float = 0.0
    ultimate_stress_mpa: float = 0.0
    fracture_strain: float = 0.0

    @property
    def is_valid(self) -> bool:
        return (
            self.youngs_modulus_gpa > 0
            and self.yield_stress_mpa > 0
            and len(self.points) > 0
        )


@dataclass
class PhaseDiagramPoint:
    """A point in a binary phase diagram."""
    temperature_k: float = 0.0
    composition: float = 0.0  # Mole fraction of component B [0, 1]
    phase: PhaseType = PhaseType.ALPHA
    gibbs_energy_j: float = 0.0


@dataclass
class AlloyComposition:
    """Composition of a multi-component alloy."""
    components: dict[str, float] = field(default_factory=dict)

    @property
    def is_valid(self) -> bool:
        if not self.components:
            return False
        total = sum(self.components.values())
        return (
            abs(total - 1.0) < 1e-6
            and all(0.0 <= v <= 1.0 for v in self.components.values())
        )


@dataclass
class MaterialProperties:
    """Collected material properties from a full analysis."""
    lattice: LatticeParameters = field(default_factory=LatticeParameters)
    stress_strain: StressStrainCurve = field(default_factory=StressStrainCurve)
    thermal_conductivity_w_mk: float = 0.0
    youngs_modulus_gpa: float = 0.0
    phase: PhaseType = PhaseType.ALPHA
    alloy: AlloyComposition = field(default_factory=AlloyComposition)
    debye_temperature_k: float = 0.0


